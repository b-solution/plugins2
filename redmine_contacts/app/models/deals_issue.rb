class DealsIssue < ActiveRecord::Base
  belongs_to :issue
  belongs_to :deal

  validate :validate_deals_issue
  attr_accessible :issue_id, :deal_id, :issue, :deal

  def validate_deals_issue
    errors.add :deal_id, :invalid if (deal_id && !deal)
  end

end
